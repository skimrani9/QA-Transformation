def resolve_heading(section: int, heading: str) -> str:
    return HEADING_ALIASES.get((section, heading), heading)


def get_section_range(content: str, section_num: int) -> tuple[int, int] | None:
    pat = rf"^## {section_num}\. .+$"
    m = re.search(pat, content, re.MULTILINE)
    if not m:
        return None
    start = m.start()
    nxt = re.search(r"^## (?:\d+\.|Complete Styled)", content[m.end():], re.MULTILINE)
    end = m.end() + nxt.start() if nxt else len(content)
    return start, end


def line_end_at(content: str, pos: int) -> int:
    le = content.find("\n", pos)
    return le if le != -1 else len(content)


def has_theory_near(content: str, pos: int) -> bool:
    return "#### Theory" in content[pos : pos + THEORY_WINDOW]


def subsection_end(content: str, heading_pos: int, section_end: int) -> int:
    nxt = re.search(r"^### ", content[heading_pos + 4 : section_end], re.MULTILINE)
    return heading_pos + 4 + nxt.start() if nxt else section_end


def find_code_insert_point(subsection: str) -> int | None:
    """Return index in subsection after last code block, before Visual result or ---."""
    if "```" not in subsection:
        return None
    last_close = subsection.rfind("```")
    if last_close == -1:
        return None
    after_code = last_close + 3
    while after_code < len(subsection) and subsection[after_code] in "\r\n":
        after_code += 1
    for marker in ("**Visual result:**", "\n---"):
        idx = subsection.find(marker, after_code)
        if idx != -1:
            return idx
    return after_code


def enhance_section_overview(content: str, section_num: int, sec_start: int, sec_end: int) -> tuple[str, int]:
    if section_num not in SECTION_OVERVIEWS:
        return content, 0
    section = content[sec_start:sec_end]
    theory_paras, why = SECTION_OVERVIEWS[section_num]
    new_block = fmt_section_overview(theory_paras, why)

    overview_m = re.search(r"^### Section Overview\s*$", section, re.MULTILINE)
    if overview_m:
        if has_theory_near(section, overview_m.start()):
            return content, 0
        abs_ov = sec_start + overview_m.start()
        rest = content[abs_ov:sec_end]
        dash = re.search(r"\n---\s*\n", rest)
        if dash:
            replace_end = abs_ov + dash.end()
        else:
            nxt = re.search(r"^### ", rest[overview_m.end() - overview_m.start() + 1 :], re.MULTILINE)
            replace_end = abs_ov + (overview_m.end() + nxt.start() if nxt else len(rest))
        return content[:abs_ov] + new_block.lstrip("\n") + content[replace_end:], 1

    title_m = re.match(r"^(## \d+\. [^\n]+\n)", section)
    if not title_m:
        return content, 0
    insert_at = sec_start + title_m.end()
    nxt_h = re.search(r"^### ", section[title_m.end():], re.MULTILINE)
    gap = section[title_m.end() : title_m.end() + nxt_h.start()] if nxt_h else ""
    stripped = gap.strip()
    if stripped and not stripped.startswith("```") and "Section Overview" not in stripped:
        intro_end = sec_start + title_m.end() + len(gap)
        return content[:sec_start + title_m.end()] + new_block + content[intro_end:], 1
    return content[:insert_at] + new_block + content[insert_at:], 1


def enhance_heading(
    content: str, section_num: int, heading: str, sec_start: int, sec_end: int
) -> tuple[str, int]:
    resolved = resolve_heading(section_num, heading)
    key = (section_num, heading)
    if key not in TOPICS:
        return content, 0

    marker = f"### {resolved}\n"
    pos = content.find(marker, sec_start, sec_end)
    if pos == -1:
        return content, 0

    enhanced = 0
    data = TOPICS[key]

    if not has_theory_near(content, pos):
        le = line_end_at(content, pos)
        content = content[: le + 1] + data["theory"] + content[le + 1 :]
        enhanced += 1
        sec_end += len(data["theory"])

    sub_end = subsection_end(content, pos, sec_end)
    subsection = content[pos:sub_end]
    if "#### How the Browser Applies This" not in subsection:
        insert_rel = find_code_insert_point(subsection)
        if insert_rel is not None:
            insert_abs = pos + insert_rel
            content = content[:insert_abs] + data["browser"] + content[insert_abs:]
            enhanced += 1

    return content, enhanced


def main() -> None:
    content = NOTES_PATH.read_text(encoding="utf-8")
    protect_idx = content.find(PROTECT_MARKER)
    if protect_idx == -1:
        protect_idx = len(content)
    editable = content[:protect_idx]
    protected = content[protect_idx:]

    total = 0
    for section_num in range(2, 21):
        bounds = get_section_range(editable, section_num)
        if not bounds:
            continue
        sec_start, sec_end = bounds
        editable, n = enhance_section_overview(editable, section_num, sec_start, sec_end)
        total += n
        bounds = get_section_range(editable, section_num)
        if not bounds:
            continue
        sec_start, sec_end = bounds
        for heading in SECTION_HEADINGS.get(section_num, []):
            editable, n = enhance_heading(editable, section_num, heading, sec_start, sec_end)
            total += n
            bounds = get_section_range(editable, section_num)
            if bounds:
                sec_start, sec_end = bounds

    NOTES_PATH.write_text(editable + protected, encoding="utf-8")
    line_count = editable.count("\n") + protected.count("\n") + (0 if editable.endswith("\n") or protected.startswith("\n") else 1)
    if not editable.endswith("\n") and not protected.startswith("\n") and protected:
        line_count = (editable + protected).count("\n") + (1 if not (editable + protected).endswith("\n") else 0)
    full = editable + protected
    line_count = len(full.splitlines())
    print(f"Enhanced {total} blocks (theory and/or browser sections).")
    print(f"Final line count: {line_count}")


