def fmt_browser(steps: list[str], items: list[tuple[str, str]]) -> str:
    numbered = "\n".join(f"{i}. {s}" for i, s in enumerate(steps, 1))
    bullets = "\n".join(f"- **{kind}:** {text}" for kind, text in items)
    return (
        f"\n#### How the Browser Applies This\n\n{numbered}\n\n"
        f"#### Common Mistakes & Best Practices\n\n{bullets}\n"
    )


